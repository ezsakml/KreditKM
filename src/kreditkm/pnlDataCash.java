    private void datacash() {
        String[] judul={"Kode Cash","Tanggal Cash","Kode Pelanggan","Kode Motor","Jumlah Beli","Bayar","Kembalian","Keterangan"}; 
        DefaultTableModel tabModel = new DefaultTableModel (null,judul);
            try {
                DB db = new DB ("jdbc:mysql://localhost:3306/kredit_km?user=root&password=");
                db.createQuery("Select * from belicash order by kodecash");
                while (db.getResult().next()) {
                    String kodecash_p = db.getResult().getString("kodecash");
                    String tglcash = db.getResult().getString("tanggalcash");
                    String kodepelanggan = db.getResult().getString("kodecust");
                    String kodemotor = db.getResult().getString("kodemotor");
                    String jumlah = db.getResult().getString("jumlah");
                    String bayar = db.getResult().getString("bayar");
                    String kembalii = db.getResult().getString("kembali");
                    String keterangan = db.getResult().getString("keterangan");
            
                    Object[] data = {kodecash_p,tglcash,kodepelanggan,kodemotor,jumlah,bayar,kembalii,keterangan};
                    tabModel.addRow(data);
                    //getAutoKodePelanggan();
                }
                db.closeResult();
                db.closeConnection();
        
                tablecash.setModel(tabModel);
                
                tablecash.getTableHeader().setFont(new Font("Segoe UI",Font.BOLD, 12));
                tablecash.getTableHeader().setOpaque(false);
                tablecash.getTableHeader().setBackground(new Color(251,253,138));
                tablecash.setRowHeight(25);
                DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
                centerRenderer.setHorizontalAlignment(JLabel.CENTER);
                for(int a=0;a<tablecash.getColumnCount();a++){
                    tablecash.getColumnModel().getColumn(a).setCellRenderer(centerRenderer);
                }
                scrolldatacash.getViewport().add(tablecash);
                scrolldatacash.setViewportView(tablecash);
                }
                catch (Exception ex) {
                     System.out.println(ex); 
                }
    }
    
