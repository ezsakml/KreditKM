    private void tablecashMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablecashMouseClicked
        Object a = tablecash.getModel().getValueAt(tablecash.getSelectedRow(), 0);
        Object b = tablecash.getModel().getValueAt(tablecash.getSelectedRow(), 1);
        Object c = tablecash.getModel().getValueAt(tablecash.getSelectedRow(), 2);
        Object d = tablecash.getModel().getValueAt(tablecash.getSelectedRow(), 3);
        Object e = tablecash.getModel().getValueAt(tablecash.getSelectedRow(), 4);
        Object f = tablecash.getModel().getValueAt(tablecash.getSelectedRow(), 5);
        Object g = tablecash.getModel().getValueAt(tablecash.getSelectedRow(), 6);
        Object p = tablecash.getModel().getValueAt(tablecash.getSelectedRow(), 7);
        kodecash.setText(a.toString());
        tglbelicash.setText(b.toString());
        kodecust.setText(c.toString());
        kodemotor.setText(d.toString());
        jmlbeli.setText(e.toString());
        bayar.setText(f.toString());
        kembali.setText(g.toString());
        ket.setText(p.toString());
        
        try {
            DB db = new DB("jdbc:mysql://localhost:3306/kredit_km?user=root&password=");
            db.createQuery("select belicash.total, pelanggan.nama, motor.namamotor, belicash.kodecash from motor,pelanggan,belicash "
                    + "where belicash.kodecust=pelanggan.kodecust "
                    + "AND belicash.kodemotor=motor.kodemotor "
                    + "AND belicash.kodecash = '"+kodecash.getText()+"'");
            while(db.getResult().next()){
                String h = db.getResult().getString("nama");
                String i = db.getResult().getString("namamotor");
                String j = db.getResult().getString("total");
                namaplg.setText(h);
                namamtr.setText(i);
                tharga.setText(j);
            }
            
            datacash();
            txtcari.setText("");
            db.closeResult();
            db.closeConnection();
        } catch (Exception ex){
            System.out.println(ex);
        }
    }//GEN-LAST:event_tablecashMouseClicked

